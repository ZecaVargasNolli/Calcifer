    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jTextArea3.setText("Analisando resultado.....");
        if(jTextArea1.getText() != "") {
            SentimentAnalyser ana =  SentimentAnalyser.getInstace();
            ana.setTextToProcess(jTextArea1.getText());
            ana.process();
            jTextArea3.setText("Negativo: " + String.valueOf(ana.getPorcentagemSentimentoNegativo()) + "% \n" +
                               "Positivo: " + String.valueOf(ana.getPorcentagemSentimentoPositivo()) + "% \n"  +
                               "Neutral: "  + String.valueOf(ana.getPorcentagemSentimentoNeutro()) + "% \n" +
                               "Resultado: " + ana.getSentimento());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

